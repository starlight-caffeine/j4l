interface AnsiColor {
    String RESET = "\u001B[0m";
    String RED_FG = "\u001b[31m";
    String CYAN_FG = "\u001b[36m";
}

static String colorize(String arg, String color) {
    return color + arg + AnsiColor.RESET;
}

